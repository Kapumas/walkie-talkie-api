import { faker } from '@faker-js/faker';

export function getUserList(usersLength = 10) {
  const users = Array.apply(null, { length: usersLength }).map((_, index) => ({
    id: index,
    name: `${faker.name.firstName()} ${faker.name.lastName()}`,
    photo: {
      url: imageArray[faker.random.numeric(1)],
    },
  }));
  return users;
}

